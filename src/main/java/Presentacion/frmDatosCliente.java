    private void btnDatosClienteGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatosClienteGuardarActionPerformed
          try {
        GuardarClienteDTO datosCliente = this.obtenerObjetoGuadarCliente();

        if (this.cliente == null) { 
            ClienteDTO nuevoCliente = this.clienteNegocio.guardar(datosCliente);
            JOptionPane.showMessageDialog(this, "Alumno guardado correctamente.", "Bien", JOptionPane.INFORMATION_MESSAGE);
        } else { // Si estamos editando un alumno existente
            ClienteDTO clienteEditado = new ClienteDTO(
                cliente.getId(),
                datosCliente.getNombres(),
                datosCliente.getApellidoPaterno(),
                datosCliente.getApellidoMaterno(),
                datosCliente.getFechaNacimiento()
            );
            this.clienteNegocio.editar(cliente.getId(), clienteEditado);
            JOptionPane.showMessageDialog(this, "Alumno actualizado correctamente.", "Bien", JOptionPane.INFORMATION_MESSAGE);
        }
     
       

    } catch (NegocioException ex) {
        JOptionPane.showMessageDialog(this, "Error al guardar el alumno: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnDatosClienteGuardarActionPerformed

