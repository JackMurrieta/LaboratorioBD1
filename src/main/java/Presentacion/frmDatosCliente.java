    public GuardarClienteDTO obtenerObjetoGuadarCliente() {
        String nombre = textDatosNombreCliente.getText();
        String apellidoPaterno = textDatosApellidoPaternoCliente.getText();
        String apellidoMaterno = textDatosApellidoMaternoCliente.getText();
         Integer idLaboratorio = 1;
        String fechaTexto = textDatosFechaNacimientoCliente.getText(); // Obtienes el texto del campo
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy"); // Asumiendo el formato "día-mes-año"
        LocalDate fechaNacimiento = LocalDate.parse(fechaTexto, formatter); // Convierte el texto a LocalDate

        return new GuardarClienteDTO(nombre, apellidoPaterno, apellidoMaterno, fechaNacimiento, idLaboratorio);
    }

    private void obtenerDatos(ClienteDTO cliente) {
        textDatosNombreCliente.setText(cliente.getNombres());
        textDatosApellidoPaternoCliente.setText(cliente.getApellidoPaterno());
        textDatosApellidoMaternoCliente.setText(cliente.getApellidoMaterno());

        // Formateo de la fecha de nacimiento a cadena
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String fechaNacimientoTexto = cliente.getFechaNacimiento().format(formatter); // Convertimos el LocalDate a String
        textDatosFechaNacimientoCliente.setText(fechaNacimientoTexto);
    }

